# No terminal, inicialize duas variáveis a e b, sendo a = 10 e b = 5.
# Mostre o resultado das 7 operações básicas
# (soma, subtração, multiplicação, divisão, divisão inteira,
# potenciação e módulo) envolvendo essas variáveis.

a = 10
b = 5

print("\nO resultado da soma é:", a + b)

print("\nO resultado da subtração é:", a - b)

print("\nO resultado da multiplicação é:", a * b)

print("\nO resultado da divisão é:", a / b)

print("\nO resultado da divisão inteira é:", a // b)

print("\nO resultado da potenciação é:", a**b)

print("\nO resultado da módulo é:", a % b, "\n")
